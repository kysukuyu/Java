public class java49{
	public static void main(String[] args){
		int x=15;
		int y=10;
		System.out.println("15>10 = " + (x>y));
		System.out.println("15<10 = " + (x<y));
		System.out.println("15>=15 = " + (x>=x));
		System.out.println("15<=15 = " + (x<=x));
		System.out.println("15==15 = " + (x==x));
		System.out.println("15==10 = " + (x==y));
		System.out.println("'x' == 'x' = "+ ('x'==x));
		System.out.println("15 != 15 = "+ (x !=x));
		System.out.println("15 != 10 = "+ (x !=y));
		System.out.println("'x' != 'x' = "+ ('x' != 'x'));
	}
}

		
		
		
		
		